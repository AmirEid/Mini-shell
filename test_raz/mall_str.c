#include <stdlib.h>

int main(void)
{
    char    *str;
    int     i = 0;
    
    str = malloc(3);
    while (i < 3)
    {
        str[i] = 'a';
        i++;
    }
    str[0] = '\0';
    free(str);
    return (0);
}